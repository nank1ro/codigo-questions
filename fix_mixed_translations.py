def fix_mixed_text(text: str) -> str:
    """Fix mixed English-Spanish text."""
    result = text

    # Apply fixes - longer phrases first
    for en, es in sorted(ENGLISH_FIXES.items(), key=lambda x: len(x[0]), reverse=True):
        # Use word boundary matching for single words
        if ' ' in en:  # Multi-word phrase
            result = result.replace(en, es)
        else:  # Single word - use word boundaries
            pattern = r'\b' + re.escape(en.strip()) + r'\b'
            result = re.sub(pattern, es.strip(), result, flags=re.IGNORECASE)

    return result

def fix_file(file_path: Path) -> bool:
    """Fix a single file."""
    try:
        content = file_path.read_text(encoding='utf-8')

        # Only fix user-facing sections
        lines = content.split('\n')
        result = []
        in_translatable_section = False
        in_code_block = False

        for line in lines:
            stripped = line.strip()

            # Check for section tags
            if stripped.startswith('# --'):
                in_translatable_section = stripped in [
                    '# --description--',
                    '# --instructions--',
                    '# --output--',
                    '# --answers--',
                ]
                result.append(line)
                continue

            # Track code blocks
            if stripped.startswith('```'):
                in_code_block = not in_code_block
                result.append(line)
                continue

            # Fix translatable sections
            if in_translatable_section and not in_code_block:
                result.append(fix_mixed_text(line))
            else:
                result.append(line)

        fixed_content = '\n'.join(result)
        file_path.write_text(fixed_content, encoding='utf-8')
        return True

    except Exception as e:
        print(f"Error fixing {file_path}: {e}")
        return False

def main():
    """Fix all translated files."""
    base_path = Path('/Users/ale/github/codigo-questions/es/swift')

    md_files = list(base_path.rglob('*.md'))
    json_files = list(base_path.rglob('data.json'))

    print(f"Fixing {len(md_files)} markdown files and {len(json_files)} JSON files...")

    fixed_count = 0

    for file_path in json_files:
        if fix_file(file_path):
            print(f"Fixed: {file_path.relative_to(base_path)}")
            fixed_count += 1

    for file_path in sorted(md_files):
        if file_path.name.startswith('_'):
            continue

        if fix_file(file_path):
            print(f"Fixed: {file_path.relative_to(base_path)}")
            fixed_count += 1

    print(f"\nFixed {fixed_count} files.")

if __name__ == '__main__':
    main()
